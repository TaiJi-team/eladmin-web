          <p><br></p>
          <p>此前，已申请将2月社会保险费延长至3月底缴纳的参保单位，2月社会保险费可延长至8月底前缴纳。</p>
          <p><br></p>
          <p>以上参保单位，其2月社会保险费不予自动扣缴，但如需继续缓缴3月及以后月份社会保险费，须按规定条件、流程和时限申请缓缴，经审核通过后，按规定缓缴。</p>
