          <p><br></p>
          <p>企业可自3月9日起通过北京市社会保险网上服务平台查询划型结果，对划型结果有异议的，应在3月底前向所属区社保经办机构提出变更申请，同时提交2019年本单位从业人数、营业收入、资产总额、2019年12月实际从业人数等材料，按规定进行复核确认。原则上，企业类型一旦划定，政策执行期间不做变动。</p>
